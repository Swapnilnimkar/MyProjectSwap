package stringStudy;

public class UpperLowerCase {

	public static void main(String[] args) {
		
		String a = "velocity";
		
	    System.out.println(a.toUpperCase());  //lower to upper
	
	    String b = "VELOCITY";
	         
	    System.out.println(b.toLowerCase());  //upper to lower
	    
	    
	         
	         
		

	}

}
